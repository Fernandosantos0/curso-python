# TIPOS DE DADOS (TIPOS DE DADOS PRIMITIVOS)

"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Fernando Santos')
print(1, 'Fernando "Santos"')
print(2, "Fernando 'Santos'")


# Aspas duplas
print("Fernando Santos")

# Escape
print("Fernando \"Santos\"")

# r
print(r"Fernando \"Santos\"")